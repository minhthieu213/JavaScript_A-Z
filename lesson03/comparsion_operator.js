// Comparsion operator: Toán tử so sánh.

let x = 10
let y = 5

console.log(x == y) // Bằng nhau: false
console.log(x === y) // Bằng nhau và phải cùng kiểu dữ liệu: false
console.log(x != y) // Không bằng nhau: true
console.log(x !== y) // Không bằng nhau và phải cùng kiểu dữ liệu: true
console.log(x > y) // Lớn hơn: true
console.log(x < y) // Nhỏ hơn: false
console.log(x >= 10) // Lớn hơn hoặc bằng: true
console.log(y <= 5) // Nhỏ hơn hoặc bằng: true